def override_settings(dottedpath):
    try:
        _m = __import__(dottedpath, fromlist=[None])
    except ImportError:
        pass
    else:
        _thismodule = sys.modules[__name__]
        for _k in dir(_m):
            if _k[:1].isupper():
                setattr(_thismodule, _k, getattr(_m, _k))

